    typedef unsigned              char     uint8_t;
#endif

#ifndef uint_16_t
    typedef unsigned    short     int      uint16_t;
#endif

#ifndef uint_32_t
    typedef unsigned              int      uint32_t;
#endif


#ifndef int8_t
    typedef               char     int8;
#endif

#ifndef int16_t
    typedef    short      int      int16;
#endif

#ifndef int32_t
    typedef               int      int32;
#endif

#ifndef str
    typedef char str;
#endif

#endif // TYPE_H
